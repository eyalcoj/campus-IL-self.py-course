def div(num1, num2):
    """divide the numbers
    :param num1: the first number
    :param num2: the second number
    :type num1: float
    :type num2: float
    :return: the divide value:
    :rtype: float
    """
    return num1 / num2


def main():
    print(div(4, 3))


if __name__ == "__main__":
    main()
